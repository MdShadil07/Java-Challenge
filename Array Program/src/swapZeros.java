

public class swapZeros {
    public static void main(String[] args) {
        int []arr= {1,6,7,9,0,3,0,0,3,7,0};
        int count=0;
        for(int i=0; i<arr.length; i++){
          
                
             if(arr[i]==0){
               
                count++;
            
             System.out.println(count);
             

            }
            
        }
        
        
        
    }
    
}
